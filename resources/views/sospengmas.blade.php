        <h3>Suar Literasi</h3>
        <p class="mb-6" style="text-align:justify !important;">

          Suar Literasi merupakan salah satu program kerja Kementerian Sosial & Pengabdian Masyarakat Badan Eksekutif Mahasiswa Universitas Surabaya yang diadakan di PUSDAKOTA (Pusat Pemberdayaan Komunitas Perkotaan) Universitas Surabaya pada bulan Maret sampai Mei 2019. Acara Suar Literasi dibagi menjadi dua jenis kegiatan, yaitu kelas sosial dan puncak Suar Literasi. Kelas sosial pertama kali dimulai pada hari Jumat, 1 Maret 2019 yang selanjutnya diadakan setiap hari Sabtu, selama 9 minggu. Pada minggu terakhir kelas sosial, kegiatan dikemas dalam bentuk acara puncak suar literasi. Anak-anak di sekitar PUSDAKOTA sangat antusias dengan acara kelas sosial ini, dapat dilihat dari perkembangan anak-anak yang hadir di tiap minggunya rata-rata semakin meningkat. Pada minggu pertama jumlah anak-anak yang mengikuti kegiatan kelas sosial sebanyak 13 orang, minggu kedua 19 orang, minggu ketiga 30 orang, minggu keempat 46 orang, minggu kelima 46 orang, minggu keenam 26 orang, minggu ketujuh 39 orang, minggu kedepalan 34 orang, minggu kesembilan 34 orang, dan di acara puncak ada 35 orang yang hadir.

          Kegiatan kelas sosial Suar Literasi diisi dengan materi materi yang berbeda untuk diajarkan kepada anak-anak, seperti sekolah mimpi, movie learning, hero puzzle dan I love my body. Selain pengajaran materi, para volunteer mahasiswa yang terlibat juga mengajar anak-anak menari dan menyanyi untuk memberikan penampilan pada acara puncak. Pada tiap minggu, di sesi terakhir diisi dengan menuliskan kesan dan pesan dari anak-anak terhadap kelas sosial Suar Literasi yang nantinya akan di kolektifkan dan dibukukan menjadi satu buku harapan karya anak-anak peserta Suar Literasi. Feedback dari anak-anak sangat positif dan mendukung acara Suar Literasi ini untuk diadakan kembali tahun depan dan harapannya kelas sosial dapat dilaksanakan dalam jangka waktu yang lebih lama.
